-- Уничтожаем старую базу, если она была, чтобы накат данных был чистым
DROP DATABASE IF EXISTS mebelorg_db;
CREATE DATABASE mebelorg_db DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE mebelorg_db;

-- =======================================================
-- 1. СОЗДАНИЕ СТРУКТУРЫ ТАБЛИЦ
-- =======================================================

-- Таблица: Пользователи
CREATE TABLE Users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    Роль_сотрудника VARCHAR(50) NOT NULL,
    ФИО VARCHAR(150) NOT NULL,
    Логин VARCHAR(100) NOT NULL UNIQUE,
    Пароль VARCHAR(100) NOT NULL
);

-- Таблица: Пункты выдачи
CREATE TABLE PickupPoints (
    id INT PRIMARY KEY,
    Адрес TEXT NOT NULL
);

-- Таблица: Товары
CREATE TABLE Products (
    Артикул VARCHAR(50) PRIMARY KEY,
    Наименование_товара TEXT NOT NULL,
    Единица_измерения VARCHAR(20) NOT NULL,
    Цена DECIMAL(10, 2) NOT NULL,
    Поставщик VARCHAR(100),
    Производитель VARCHAR(100),
    Категория_товара VARCHAR(100),
    Действующая_скидка INT DEFAULT 0,
    Кол_во_на_складе INT DEFAULT 0,
    Описание_товара TEXT,
    Фото VARCHAR(100)
);

-- Таблица: Заказы
CREATE TABLE Orders (
    Номер_заказа INT PRIMARY KEY,
    Артикул_заказа TEXT NOT NULL,
    Дата_заказа DATE NOT NULL,
    Дата_доставки DATE NOT NULL,
    Пункт_выдачи_id INT,
    ФИО_клиента VARCHAR(150),
    Код_для_получения INT,
    Статус_заказа VARCHAR(50) NOT NULL,
    FOREIGN KEY (Пункт_выдачи_id) REFERENCES PickupPoints(id) ON DELETE SET NULL
);


